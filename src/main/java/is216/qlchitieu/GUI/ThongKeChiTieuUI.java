    public ArrayList<String>arrThangThongKe = new ArrayList<String>();
    public void loadThangThongKe(String nguoiDung){
        try{
            String strSQL = "select ngaychi from thongtinchitieu where tendangnhap = '"+nguoiDung+"' order by ngaychi desc";
            Connection con = new DBUtils().createConn();
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery(strSQL);
            while(rs.next()){
                String[] row = rs.getDate("ngaychi").toString().split("-");
                String temp = row[1]+"/"+row[0];
                if(!arrThangThongKe.contains(temp))
                    arrThangThongKe.add(temp);
            }
            cbbThang.setModel(new DefaultComboBoxModel<String>(arrThangThongKe.toArray(new String[0])));
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    public String dinhDangTienTe(double soTien){
        Locale localeEN = new Locale("en", "EN");
        NumberFormat en = NumberFormat.getInstance(localeEN);
        String str = en.format(soTien);
        return str;
    }
    
    ArrayList<ChiTieuDTO> arrChiTieuThang = new ArrayList<ChiTieuDTO>();
    public void updateLuongTienChiTieu(){
        ChiTieuBLL chiTieuBll = new ChiTieuBLL();
        arrChiTieuThang = chiTieuBll.getAllChiTieuByUser(tenDangNhap);
        
        ChiTieuDTO chitieu = new ChiTieuDTO();
        ArrayList<String> arrMaChiTieu = new ArrayList<String>();
        for(int i=0; i<arrChiTieuThang.size();i++){
            chitieu = arrChiTieuThang.get(i);
            if(!arrMaChiTieu.contains(chitieu.getMaChiTieu())){
                String machitieu = chitieu.getMaChiTieu();
                arrMaChiTieu.add(machitieu);
                chiTieuBll.updateLuongTienById(machitieu);
            }
        }
    }
