    private void cbbThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbThangActionPerformed
        // TODO add your handling code here:
        tblModelChiTieu.setRowCount(0);
        ChiTieuBLL chiTieuBll = new ChiTieuBLL();
        String[]date = this.cbbThang.getSelectedItem().toString().split("/");
        arrChiTieuThang = chiTieuBll.getAllChiTieuByMonthUser(Integer.parseInt(date[0]), Integer.parseInt(date[1]), tenDangNhap);
        
        
        ChiTieuDTO chitieu = new ChiTieuDTO();
        ArrayList<String> arrMaChiTieu = new ArrayList<String>();
        double tongTienDaChi = 0;
        double luongTienMax = 0;
        for(int i=0; i<arrChiTieuThang.size();i++){
            chitieu = arrChiTieuThang.get(i);
            if(!arrMaChiTieu.contains(chitieu.getMaChiTieu())){
                String machitieu = chitieu.getMaChiTieu();
                arrMaChiTieu.add(machitieu);
                
                
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                String ngaygiaodich = sdf.format(chitieu.getNgayChi());
                
                
                String luongtien = dinhDangTienTe(chitieu.getLuongTien());
                tongTienDaChi+=chitieu.getLuongTien();
                if(luongTienMax<chitieu.getLuongTien()){
                    luongTienMax = chitieu.getLuongTien();
                    dateNgaySDNhieuNhat.setDate(chitieu.getNgayChi());
                }
                Object[] row = {machitieu, ngaygiaodich, luongtien};
                tblModelChiTieu.addRow(row);
            }  
        }
        txtTongTien.setText(dinhDangTienTe(tongTienDaChi));
        tblChiTieu.setModel(tblModelChiTieu);
        GioiHanChiTieuBLL ghctBll = new GioiHanChiTieuBLL();
        GioiHanChiTieuDTO ghct = ghctBll.getGioiHanChiTieuByTenDangNhap(tenDangNhap);
        txtCachHanMuc.setText(dinhDangTienTe(ghct.getGioiHan()-tongTienDaChi));
    }//GEN-LAST:event_cbbThangActionPerformed

