    private void btnChiTietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChiTietActionPerformed
        // TODO add your handling code here:
        tblModelTieuDung.setRowCount(0);
        int indexTB = tblChiTieu.getSelectedRow();
        ArrayList<TieuDungDTO> arrTieuDung = new ArrayList<TieuDungDTO>();
        if(indexTB<tblModelChiTieu.getRowCount() && indexTB>=0){
            TieuDungBLL tieuDungBll = new TieuDungBLL();
            arrTieuDung = tieuDungBll.getAllTieuDungByMaChiTieu(tblModelChiTieu.getValueAt(indexTB, 0).toString());
        }
        TieuDungDTO tieuDung = new TieuDungDTO();
        int stt =0;
        for(int i=0; i<arrTieuDung.size(); i++){
            tieuDung = arrTieuDung.get(i);
            stt +=1;
            String tentieudung = tieuDung.getTenHangHoa();
            int soluong = tieuDung.getSoLuong();
            String thanhtien = dinhDangTienTe(tieuDung.getThanhTien());
            Object[] row = {stt, tentieudung, soluong, thanhtien};
            tblModelTieuDung.addRow(row);
        }
        tblTieuDung.setModel(tblModelTieuDung);
        tabpanel.setSelectedIndex(1);
    }//GEN-LAST:event_btnChiTietActionPerformed

